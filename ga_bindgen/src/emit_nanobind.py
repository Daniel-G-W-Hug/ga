"""Emit nanobind C++ bindings from a manifest.

Two modes:
  --type NAME     Emit a single type (for quick iteration).
  --all           Emit every eligible type in the manifest plus register_all.cpp
                  and bindings_list.cmake.

Eligibility (v1): struct/class with ≥1 public T-typed field and at least one
all-T-parameter constructor (after normalizing references/const). Scalar/
PScalar types (private `value` field + explicit operator T&) are not yet
handled — deferred to a later iteration.

Usage:  python3 ga_bindgen/src/emit_nanobind.py --all
"""
